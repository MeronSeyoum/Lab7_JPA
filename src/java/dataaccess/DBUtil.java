package dataaccess;

import java.sql.ResultSet;
import java.sql.SQLException;

/**
 *
 * @author merya
 */
public class DBUtil  {
 public static void closePreparedStatement(java.sql.Statement ps) {
        try {
            if (ps != null) {
                ps.close();
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

    public static void closeResultSet(ResultSet rs) {
        try {
            if (rs != null) {
                rs.close();
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }
}
